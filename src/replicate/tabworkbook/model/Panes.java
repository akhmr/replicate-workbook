	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getY_axis_name() {
		return y_axis_name;
	}

	public void setY_axis_name(String y_axis_name) {
		this.y_axis_name = y_axis_name;
	}
	
	public String getX_axis_name() {
		return x_axis_name;
	}

	public void setX_axis_name(String x_axis_name) {
		this.x_axis_name = x_axis_name;
	}
	
	public String getColumn() {
		return column;
	}

	public void setColumn(String column) {
		this.column = column;
	}

	public String getRows() {
		return rows;
	}

	public void setRows(String rows) {
		this.rows = rows;
	}

	public String getColumns() {
		return columns;
	}

	public void setColumns(String columns) {
		this.columns = columns;
	}

	
